    public function save(Request $request){
        $idUser = Auth::user()->id;
        $cart = Cart::all()->where('user_id','=',$idUser)->Where('status','=',0);
        foreach ($cart as $c) {
            $c->save();
        }
        return response()->json($cart);
    }

